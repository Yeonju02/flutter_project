import 'package:flutter/material.dart';
import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:intl/intl.dart';
import 'package:routinelogapp/shop/payment.dart';

class CartPage extends StatefulWidget {
  final String userId;
  const CartPage({super.key, required this.userId});

  @override
  State<CartPage> createState() => _CartPageState();
}

class _CartPageState extends State<CartPage> {
  Map<String, int> quantities = {};
  Map<String, bool> selected = {};
  final formatter = NumberFormat('#,###');
  bool get _isAllSelected => selected.values.every((e) => e);

  @override
  Widget build(BuildContext context) {
    return FutureBuilder<QuerySnapshot>(
      future: FirebaseFirestore.instance
          .collection('users')
          .where('userId', isEqualTo: widget.userId)
          .limit(1)
          .get(),
      builder: (context, userSnapshot) {
        if (!userSnapshot.hasData) {
          return const Scaffold(
            body: Center(child: CircularProgressIndicator()),
          );
        }

        if (userSnapshot.data!.docs.isEmpty) {
          return const Scaffold(
            body: Center(child: Text('ÏÇ¨Ïö©Ïûê Ï†ïÎ≥¥Î•º Ï∞æÏùÑ Ïàò ÏóÜÏäµÎãàÎã§.')),
          );
        }

        final userDocId = userSnapshot.data!.docs.first.id;

        final cartRef = FirebaseFirestore.instance
            .collection('users')
            .doc(userDocId)
            .collection('cart')
            .orderBy('addedAt', descending: true);

        return Scaffold(
          backgroundColor: Colors.white,
          appBar: AppBar(
            title: const Text('Ïû•Î∞îÍµ¨Îãà', style: TextStyle(fontWeight: FontWeight.bold, fontSize: 18)),
            backgroundColor: Colors.white,
            leading: const BackButton(),
            actions: [
              TextButton(
                onPressed: () async {
                  final selectedIds = selected.entries
                      .where((entry) => entry.value)
                      .map((entry) => entry.key)
                      .toList();

                  for (final docId in selectedIds) {
                    await FirebaseFirestore.instance
                        .collection('users')
                        .doc(userDocId)
                        .collection('cart')
                        .doc(docId)
                        .delete();
                  }

                  setState(() {
                    for (final id in selectedIds) {
                      selected.remove(id);
                      quantities.remove(id);
                    }
                  });
                },
                child: const Text('ÏÑ†ÌÉù ÏÇ≠Ï†ú', style: TextStyle(color: Colors.red)),
              ),
              TextButton(
                onPressed: () {
                  final newValue = !_isAllSelected;
                  setState(() {
                    for (var key in selected.keys) {
                      selected[key] = newValue;
                    }
                  });
                },
                child: Text(_isAllSelected ? 'Ï†ÑÏ≤¥ Ìï¥Ï†ú' : 'Ï†ÑÏ≤¥ ÏÑ†ÌÉù'),
              ),
            ],
          ),
          body: StreamBuilder<QuerySnapshot>(
            stream: cartRef.snapshots(),
            builder: (context, snapshot) {
              if (!snapshot.hasData) return const Center(child: CircularProgressIndicator());

              final docs = snapshot.data!.docs;
              if (docs.isEmpty) return const Center(child: Text('Ïû•Î∞îÍµ¨ÎãàÍ∞Ä ÎπÑÏñ¥ ÏûàÏäµÎãàÎã§'));

              for (var doc in docs) {
                final id = doc.id;
                final data = doc.data() as Map<String, dynamic>;
                quantities[id] ??= data['quantity'] ?? 1;
                selected[id] ??= true;
              }

              final selectedDocs = docs.where((doc) => selected[doc.id] == true).toList();
              final total = selectedDocs.fold<int>(
                0,
                    (sum, doc) => sum + (doc['productPrice'] as int) * (quantities[doc.id] ?? 1),
              );

              // üëá Ïù¥Ìïò Í∏∞Ï°¥ UI Í∑∏ÎåÄÎ°ú...
              return Column(
                children: [
                  Expanded(
                    child: GridView.builder(
                      padding: const EdgeInsets.all(12),
                      gridDelegate: const SliverGridDelegateWithFixedCrossAxisCount(
                        crossAxisCount: 2,
                        crossAxisSpacing: 12,
                        mainAxisSpacing: 12,
                        childAspectRatio: 0.65,
                      ),
                      itemCount: docs.length,
                      itemBuilder: (context, index) {
                        final doc = docs[index];
                        final data = doc.data() as Map<String, dynamic>;
                        final docId = doc.id;
                        final quantity = quantities[docId]!;
                        final isSelected = selected[docId]!;

                        return Container(
                          padding: const EdgeInsets.all(8),
                          decoration: BoxDecoration(
                            color: Color(0xFFF0F4FA),
                            borderRadius: BorderRadius.circular(12),
                          ),
                          child: Column(
                            crossAxisAlignment: CrossAxisAlignment.start,
                            children: [
                              // Ï≤¥ÌÅ¨Î∞ïÏä§ (Ïù¥ÎØ∏ÏßÄ ÏúÑ)
                              Row(
                                children: [
                                  Checkbox(
                                    value: isSelected,
                                    onChanged: (val) {
                                      setState(() {
                                        selected[docId] = val ?? false;
                                      });
                                    },
                                  ),
                                ],
                              ),

                              // Ïù¥ÎØ∏ÏßÄ
                              Stack(
                                children: [
                                  ClipRRect(
                                    borderRadius: BorderRadius.circular(10),
                                    child: Image.network(
                                      data['thumbNail'],
                                      width: double.infinity,
                                      height: 100,
                                      fit: BoxFit.cover,
                                    ),
                                  ),
                                ],
                              ),

                              const SizedBox(height: 8),
                              Text(
                                data['productName'],
                                maxLines: 1,
                                overflow: TextOverflow.ellipsis,
                                style: const TextStyle(fontWeight: FontWeight.bold),
                              ),
                              const SizedBox(height: 4),
                              Text('${formatter.format(data['productPrice'])}Ïõê'),
                              const SizedBox(height: 4),
                              // ÏÉâÏÉÅ ÌëúÏãú
                              Text('ÏÉâÏÉÅ: ${_getColorLabel(data['selectedColor'] ?? 'Í∏∞Î≥∏ÏÉâ')}'),
                              const SizedBox(height: 4),

                              // ÏàòÎüâ Ï°∞Ï†à
                              Row(
                                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                                children: [
                                  IconButton(
                                    icon: const Icon(Icons.remove, size: 16),
                                    onPressed: () async {
                                      if (quantities[docId]! > 1) {
                                        setState(() {
                                          quantities[docId] = quantities[docId]! - 1;
                                        });

                                        await FirebaseFirestore.instance
                                            .collection('users')
                                            .doc(widget.userId)
                                            .collection('cart')
                                            .doc(docId)
                                            .update({'quantity': quantities[docId]});
                                      }
                                    },
                                  ),
                                  Text('$quantity'),
                                  IconButton(
                                    icon: const Icon(Icons.add, size: 16),
                                    onPressed: () async {
                                      setState(() {
                                        quantities[docId] = quantities[docId]! + 1;
                                      });

                                      await FirebaseFirestore.instance
                                          .collection('users')
                                          .doc(widget.userId)
                                          .collection('cart')
                                          .doc(docId)
                                          .update({'quantity': quantities[docId]});
                                    },
                                  ),
                                ],
                              ),
                            ],
                          ),
                        );
                      },
                    ),
                  ),

                  // ÌïòÎã® Í≤∞Ï†ú ÏöîÏïΩ
                  Padding(
                    padding: const EdgeInsets.symmetric(horizontal: 30, vertical: 20),
                    child: Row(
                      children: [
                        Expanded(
                          child: Text('Ï¥ù ${selectedDocs.length}Í∞ú Ï£ºÎ¨∏Í∏àÏï°\n${formatter.format(total)}Ïõê',
                              style: const TextStyle(fontWeight: FontWeight.bold)),
                        ),
                        ElevatedButton.icon(
                          onPressed: () {
                            // ÏÑ†ÌÉùÎêú ÏÉÅÌíà Î¶¨Ïä§Ìä∏ Ï∂îÏ∂ú
                            final selectedItems = selectedDocs.map((doc) {
                              final data = doc.data() as Map<String, dynamic>;
                              final docId = doc.id;

                              return {
                                'productId': data['productId'],
                                'productName': data['productName'],
                                'productPrice': data['productPrice'],
                                'thumbNail': data['thumbNail'],
                                'quantity': quantities[docId],
                                'selectedColor': data['selectedColor'] ?? 'Í∏∞Î≥∏ÏÉâ', // ÏóÜÏúºÎ©¥ Í∏∞Î≥∏Í∞í Ï≤òÎ¶¨
                              };
                            }).toList();

                            if (selectedItems.isEmpty) {
                              ScaffoldMessenger.of(context).showSnackBar(
                                const SnackBar(content: Text("Í≤∞Ï†úÌï† ÏÉÅÌíàÏùÑ ÏÑ†ÌÉùÌï¥Ï£ºÏÑ∏Ïöî.")),
                              );
                              return;
                            }

                            Navigator.push(
                              context,
                              MaterialPageRoute(
                                builder: (context) => PaymentPage(products: selectedItems),
                              ),
                            );
                          },
                          icon: const Icon(Icons.payment),
                          label: const Text('Ï£ºÎ¨∏ÌïòÍ∏∞'),
                          style: ElevatedButton.styleFrom(
                            backgroundColor: Color(0xFF92BBE2),
                            foregroundColor: Colors.white,
                            padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 16),
                            shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(10)),
                          ),
                        )
                      ],
                    ),
                  )
                ],
              );
            },
          ),
        );
      },
    );
  }
  String _getColorLabel(String id) {
    switch (id.toLowerCase()) {
      case 'blue':
        return 'Î∏îÎ£®';
      case 'green':
        return 'Í∑∏Î¶∞';
      case 'pink':
      case 'pinkaccent':
        return 'ÌïëÌÅ¨';
      case 'red':
        return 'Î†àÎìú';
      case 'navy':
        return 'ÎÑ§Ïù¥ÎπÑ';
      case 'black':
        return 'Î∏îÎûô';
      case 'white':
        return 'ÌôîÏù¥Ìä∏';
      case 'gray':
      case 'grey':
        return 'Í∑∏Î†àÏù¥';
      case 'orange':
        return 'Ïò§Î†åÏßÄ';
      case 'yellow':
        return 'ÏòêÎ°úÏö∞';
      case 'beige':
        return 'Î≤†Ïù¥ÏßÄ';
      case 'purple':
        return 'ÌçºÌîå';
      case 'lavender':
        return 'ÎùºÎ≤§Îçî';
      case 'skyblue':
        return 'Ïä§Ïπ¥Ïù¥ Î∏îÎ£®';
      case 'lime':
        return 'ÎùºÏûÑ';
      case 'olive':
        return 'Ïò¨Î¶¨Î∏å';
      case 'gold':
        return 'Í≥®Îìú';
      default:
        return id;
    }
  }

}


